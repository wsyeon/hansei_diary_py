import turtle as t

t.shape("turtle")
t.penup()
t.goto(-200, 0)
t.right(90)
t.pendown()
t.color("red")
t.fillcolor("red")
t.begin_fill()
t.circle(100, 180)
t.circle(-100, 180)
t.left(180)
t.circle(200, 180)
t.end_fill()
t.color("blue")
t.begin_fill()
t.circle(100, 180)
t.circle(-100, 180)
t.circle(-200, 180)
t.end_fill()